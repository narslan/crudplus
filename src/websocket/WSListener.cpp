oatpp::async::CoroutineStarter WSListener::onPing(const std::shared_ptr<AsyncWebSocket>& socket,
                                                  const oatpp::String& message) {
  OATPP_LOGd(TAG, "onPing");
  return socket->sendPongAsync(message);
}

oatpp::async::CoroutineStarter WSListener::onPong(const std::shared_ptr<AsyncWebSocket>& socket,
                                                  const oatpp::String& message) {
  OATPP_LOGd(TAG, "onPong");
  return nullptr; // do nothing
}

oatpp::async::CoroutineStarter WSListener::onClose(const std::shared_ptr<AsyncWebSocket>& socket,
                                                   v_uint16 code,
                                                   const oatpp::String& message) {
  OATPP_LOGd(TAG, "onClose code=%d", code);
  return nullptr; // do nothing
}

oatpp::async::CoroutineStarter
WSListener::readMessage(const std::shared_ptr<AsyncWebSocket>& socket,
                        v_uint8 opcode,
                        p_char8 data,
                        oatpp::v_io_size size) {

  if(size == 0) { // message transfer finished

    auto wholeMessage = m_messageBuffer.toString();
    m_messageBuffer.setCurrentPosition(0);

    OATPP_LOGd(TAG, "onMessage message='{}'", wholeMessage->c_str());

    /* Send message in reply */
    return socket->sendOneFrameTextAsync("Hello from oatpp!: " + wholeMessage);
  }
  else if(size > 0) { // message frame received
    m_messageBuffer.writeSimple(data, size);
  }

  return nullptr; // do nothing
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// WSInstanceListener

std::atomic<v_int32> WSInstanceListener::SOCKETS(0);

void WSInstanceListener::onAfterCreate_NonBlocking(
    const std::shared_ptr<WSListener::AsyncWebSocket>& socket,
    const std::shared_ptr<const ParameterMap>& params) {

  SOCKETS++;
  OATPP_LOGd(TAG, "New Incoming Connection. Connection count={}", SOCKETS.load());

  /* In this particular case we create one WSListener per each connection */
  /* Which may be redundant in many cases */
  socket->setListener(std::make_shared<WSListener>());
}

void WSInstanceListener::onBeforeDestroy_NonBlocking(
    const std::shared_ptr<WSListener::AsyncWebSocket>& socket) {

  SOCKETS--;
  OATPP_LOGd(TAG, "Connection closed. Connection count={}", SOCKETS.load());
}